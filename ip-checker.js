const NSLookup = async (name, type = "A") => {
  const url = "https://dns.google.com/resolve?name=" + name + "&type=" + type;
  const response = await fetch(url);
  const responseCode = await response.status;
  // console.log("responseCode: ", responseCode);
  if (responseCode !== 200) {
    throw new Error(responseCode + ": " + response.message);
  }
  const json = await response.json();
  try {
    const answers = json.Answer.filter((ans) => ans.type === 1)
      .map((ans) => ans.data)
      .join("\n");
    return answers;
  } catch {
    return json.Comment;
  }
};

// convert dotted quad to single integers
const ipv4ToInteger = (ipaddress) =>
  ipaddress
    .split(".")
    .reverse()
    .reduce((acc, part, i) => (acc += part * 256 ** i), 0);

const UkrainianIP = (ipaddress) => {
  return ipaddress
    .split("\n")
    .map((addr) => {
      const integerIp = ipv4ToInteger(addr);
      if (
        ukrainianIpRanges.some(
          ([start, finish]) => start <= integerIp && integerIp <= finish,
        )
      )
        return addr + " => Ukranian IP";
      if (
        cloudflareIpRanges.some(
          ([start, finish]) => start <= integerIp && integerIp <= finish,
        )
      )
        return addr + " => cloudflare (assume true)";
      if (
        fastlyIpRanges.some(
          ([start, finish]) => start <= integerIp && integerIp <= finish,
        )
      )
        return addr + " => fastly (assume true)";
      if (
        cloudfrontIpRanges.some(
          ([start, finish]) => start <= integerIp && integerIp <= finish,
        )
      )
        return addr + " => aws cloudfront (assume true)";
      return addr + " => false";
    })
    .join("\n");
};

